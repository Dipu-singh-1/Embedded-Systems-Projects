void led_task_handler1(void *params){

	UBaseType_t p1, p2;

	while(1){

		if(switch_priority == TRUE){
			switch_priority = FALSE;
			p1 = uxTaskPriorityGet(led_task1);
			p2 = uxTaskPriorityGet(led_task2);
			vTaskPrioritySet(led_task1, p2 );
			vTaskPrioritySet(led_task2, p1 );
		}
		else{
			HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_15);
			rtos_delay(100);
		}

	}
}

void led_task_handler2(void *params){

	UBaseType_t p1, p2;

	while(1){

		if(switch_priority == TRUE){
			switch_priority = FALSE;
			p1 = uxTaskPriorityGet(led_task1);
			p2 = uxTaskPriorityGet(led_task2);
			vTaskPrioritySet(led_task1, p2 );
			vTaskPrioritySet(led_task2, p1 );
		}else{
			HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_15);
			rtos_delay(1000);
		}

	}
}

void Interrupt_EXTI_PA0_init(void){


	// 1. Configure the external interrupt
	// external interrupt used is EXT0, GPIOA
	EXTI_ConfigTypeDef pExtiConfig;

	__HAL_RCC_SYSCFG_CLK_ENABLE();

	hexti0.Line = EXTI_LINE_0;

	pExtiConfig.GPIOSel = EXTI_GPIOA;
	pExtiConfig.Line = EXTI_LINE_0;
	pExtiConfig.Mode = EXTI_MODE_INTERRUPT;
	pExtiConfig.Trigger = EXTI_TRIGGER_RISING;

	HAL_EXTI_SetConfigLine(&hexti0, &pExtiConfig);

	// enable IRQ for external interrupt
	NVIC_SetPriority(EXTI0_IRQn, 5);
	NVIC_EnableIRQ(EXTI0_IRQn);

}


void EXTI0_IRQHandler(void){

	HAL_EXTI_ClearPending(&hexti0, EXTI_TRIGGER_RISING_FALLING);
	switch_priority = TRUE;

}

void rtos_delay(uint32_t delay_in_ms){

	uint32_t tick_count_local=0;

	//converting ms to ticks
	uint32_t delay_in_ticks = ( delay_in_ms  * configTICK_RATE_HZ ) / 1000;

	tick_count_local = xTaskGetTickCount();
	while ( xTaskGetTickCount() < (tick_count_local+delay_in_ticks ));
}

